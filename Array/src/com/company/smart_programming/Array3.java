package com.company.smart_programming;

public class Array3 {

	/**
	 * MultiDimensional Array
	 * 
	 * 2D ARRAY 3D ARRAY
	 * 
	 * ARRAY OF ARRAYS IT WILL create 2 blocks in which first block pointing to one
	 * array and 2nd block is pointing to another array
	 * 
	 * 
	 */

	/**
	 * Declare
	 * 
	 * int[][] a; we can declare in multiple ways
	 * 
	 * int [][]a;
	 * 
	 * int[] []a;
	 * 
	 * int[] a[];
	 * 
	 * #using 2 variables;
	 *
	 * int[] a,b;//a is 1d array,b is also 1d array
	 *
	 * int a[],b;//a is 1d array,b is not an array
	 *
	 * int[][] a,b;a-->2d b-->2d
	 * 
	 * int[] a[],b;a-->2d b-->1d
	 * 
	 * int[] a[],b[];a-->2d b-->2d
	 * 
	 * int[][] a,b[];a-2d b-3d
	 * 
	 * int[][]a,[]b;a-2D b//compile time error if we provide dimension before for
	 * 2nd variable it will give compile time error
	 *
	 * 
	 */

	/**
	 * 
	 * creation- a=new int[2][3];
	 * 
	 * if no of columns are same then it is called as matrix array
	 * 
	 * int[][] a;
	 * a=new int[2][];
	 * 
	 * a[0]=new int[3];
	 * 
	 * a[1]=new int[2];
	 * @implNote no of rows are provided but no of columns are not provided 
	 * 
	 */
}
