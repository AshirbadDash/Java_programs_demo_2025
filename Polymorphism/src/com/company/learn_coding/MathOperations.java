package com.company.learn_coding;

class MathOperations {

	// Method with two integers
	int add(int a, int b) {
		return a + b;
	}

	void sum(int a, int b) {
		int c = a - b;

	}

	// Method with three integers (Different number of parameters)
	int add(int a, int b, int c) {
		return a + b + c;
	}

	// Method with double parameters (Different data type)
	double add(double a, double b) {
		return a + b;
	}

	// Method with different parameter order
	String add(String a, int b) {
		return a + b;
	}
}
